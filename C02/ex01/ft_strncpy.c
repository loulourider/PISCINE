#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (src[i] != '\0' && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int	main(void)
{
	char	destination[10] = "";
	char	source[10] = "abc";

	printf("  %s  ", destination);
	ft_strncpy(destination, source, 2);
	printf("  %s  ", destination);
	return (0);
}
